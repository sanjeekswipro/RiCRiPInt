/* A little self-contained multiply with carry pseudo-random number
   generator */

static SYSTEMVALUE xps_wm_rand( uint32 *x , uint32 *c )
{
  const uint32 a = 0x7f13ac69 ;
  const uint32 ah = 0x7f13 ;
  const uint32 al = 0xac69 ;
  uint32 xh = *x >> 16 ;
  uint32 xl = *x & 0xffff ;

  *x = *x * a + *c ;
  *c = xh * ah + (( xh * al ) >> 16 ) + (( xl * ah ) >> 16 ) ;
  if ( xl * al >= ~*c + 1 ) {
    ( *c )++ ;
  }

  return ( SYSTEMVALUE )*x / 4294967296.0 ; /* 0x100000000 */
}

